namespace Steganography.IO
{
	/// <summary>
	/// First 72 digits for e(Euler's number)
	/// </summary>
	/// <remarks>
	/// http://oeis.org/A001113/list
	/// </remarks>
	public class EulersNumberKeySequence : ByteArrayKeySequence
	{
		/// <summary>
		/// Constructor which initializes with the first 72 digits for e(Euler's number)
		/// </summary>
		public EulersNumberKeySequence()
			: base(new byte[] 
			{ 
				// 2 7 1			8 2 8				1 8 2				8 4 5
				0x02, 0x07, 0x01,	0x08, 0x02, 0x08,	0x01, 0x08, 0x02,	0x08, 0x04, 0x05, 
				// 9 0 4			5 2 3				5 3 6				0 2 8
				0x09, 0x00, 0x04,	0x05, 0x02, 0x03,	0x05, 0x03, 0x06,	0x00, 0x02, 0x08, 
				// 7 4 7			1 3 5				2 6 6				2 4 9
				0x07, 0x04, 0x07,	0x01, 0x03, 0x05,	0x02, 0x06, 0x06,	0x02, 0x04, 0x09, 

				// 7 7 5			7 2 4				7 0 9				3 6 9
				0x07, 0x07, 0x05,	0x07, 0x02, 0x04,	0x07, 0x00, 0x09,	0x03, 0x06, 0x09, 
				// 9 9 5			9 5 7				4 9 6				6 9 6
				0x09, 0x09, 0x05,	0x09, 0x05, 0x07,	0x04, 0x09, 0x06,	0x06, 0x09, 0x06, 
				// 7 6 2			7 7 2				4 0 7				6 6 3
				0x07, 0x06, 0x02,	0x07, 0x07, 0x02,	0x04, 0x00, 0x07,	0x06, 0x06, 0x03, 
			})
		{
		}
	}
}
