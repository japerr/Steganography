namespace Steganography.IO.Sequences
{
	/// <summary>
	/// 72 digits of Pi
	/// </summary>
	/// <remarks>
	/// http://www.geom.uiuc.edu/~huberty/math5337/groupe/digits.html
	/// </remarks>
	public class PiKeySequence : ByteArrayKeySequence
	{
		/// <summary>
		/// Constructor which initializes with the first 72 digits for pi
		/// </summary>
		public PiKeySequence()
			: base(new byte[] 
			{ 
				// 3 1 4			1 5 9				2 6 5				3 5 8
				0x03, 0x01, 0x04,	0x01, 0x05, 0x09,	0x02, 0x06, 0x05,	0x03, 0x05, 0x08, 
				// 9 7 9			3 2 3				8 4 6				2 6 4
				0x09, 0x07, 0x09,	0x03, 0x02, 0x03,	0x08, 0x04, 0x06,	0x02, 0x06, 0x04, 
				// 3 3 8			3 2 7				9 5 0				2 8 8
				0x03, 0x03, 0x08,	0x03, 0x02, 0x07,	0x09, 0x05, 0x00,	0x02, 0x08, 0x08, 

				// 4 1 9			7 1 6				9 3 9				9 3 7
				0x04, 0x01, 0x09,	0x07, 0x01, 0x06,	0x09, 0x03, 0x09,	0x09, 0x03, 0x07, 
				// 5 1 0			5 8 2				0 9 7				4 9 4
				0x05, 0x01, 0x00,	0x05, 0x08, 0x02,	0x00, 0x09, 0x07,	0x04, 0x09, 0x04, 
				// 4 5 9			2 3 0				7 8 1				6 4 0
				0x04, 0x05, 0x09,	0x02, 0x03, 0x00,	0x07, 0x08, 0x01,	0x06, 0x04, 0x00, 
			})
		{
		}
	}
}
